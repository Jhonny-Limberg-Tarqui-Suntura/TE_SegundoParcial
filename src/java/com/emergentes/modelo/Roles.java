package com.emergentes.modelo;

public class Roles {
   
    private int id;
    private String descripcion;

    public Roles() {
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getId() {
        return id;
    }

    public String getDescripcion() {
        return descripcion;
    }
        
}
